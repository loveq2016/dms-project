package dms.yijava.api.web.model.flow;

public class StepModel {

	private String action_id;
	private String step_id;
	private String flow_id;	
	private String step_order_no;
	private String action_name;
	private String action_desc;	
	private String step_type;
	
	
	
	public String getAction_id() {
		return action_id;
	}
	public void setAction_id(String action_id) {
		this.action_id = action_id;
	}
	public String getStep_id() {
		return step_id;
	}
	public void setStep_id(String step_id) {
		this.step_id = step_id;
	}
	public String getStep_order_no() {
		return step_order_no;
	}
	public void setStep_order_no(String step_order_no) {
		this.step_order_no = step_order_no;
	}
	public String getAction_name() {
		return action_name;
	}
	public void setAction_name(String action_name) {
		this.action_name = action_name;
	}
	public String getAction_desc() {
		return action_desc;
	}
	public void setAction_desc(String action_desc) {
		this.action_desc = action_desc;
	}
	public String getStep_type() {
		return step_type;
	}
	public void setStep_type(String step_type) {
		this.step_type = step_type;
	}
	public String getFlow_id() {
		return flow_id;
	}
	public void setFlow_id(String flow_id) {
		this.flow_id = flow_id;
	}
	

	
	
}
